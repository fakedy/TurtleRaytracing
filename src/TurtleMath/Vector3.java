package TurtleMath;

public class Vector3{

    public Vector3(float x, float y, float z){
        this.x = x;
        this.y = y;
        this.z = z;

        this.r = x;
        this.g = y;
        this.b = z;
    }

    public Float x;
    public Float y;
    public Float z;

    public Float r;
    public Float g;
    public Float b;

}